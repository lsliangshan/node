/**
 * Object.defineProperty
 *
 * @param {any} obj
 * @param {any} property
 * @param {any} value
 * @param {boolean} [setter=false]
 * @returns
 */
const define = function (obj, property, value, setter = false) {
  if (setter) {
    return Object.defineProperty(obj, property, {
      value: value,
      writable: true,
      configurable: false,
      enumerable: true
    });
  } else {
    return Object.defineProperty(obj, property, {
      get: function () {
        return value;
      },
      configurable: false,
      enumerable: true
    });
  }
};

module.exports = {
  define: define
}